package start;

public class DataTypes {

	public static void main(String[] args) {
		byte a = 10; // 1 byte
		short b = 11; // 2 bytes
		int c = 12; // 4 bytes
		long d = 13; // 8 bytes
		float e = 14.4f; //  4 bytes
		double f = 123.45d; // 8 bytes
		char g = 'A'; // 2 bytes from 0 to 65535
		boolean h = false; // only true or false values
		System.out.println(a+" "+b+" "+c+" "+d+" "+e+" "+f+" "+g+" "+h);
	}

}
