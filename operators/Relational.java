package operators;

public class Relational {

	public static void main(String[] args) {
		int a = 10, b= 11;
		System.out.println(a<b);//true, these are come under relational because of comparing the values
		System.out.println(a>b);//false but not unary due to two variables
		System.out.println(a==b);//false
		System.out.println(a<=b);//true
		System.out.println(a>=b);//false
	}

}
